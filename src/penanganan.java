    private void datatable(){
        Object[] Baris = {"Kode Penanganan","Kode Petugas","Nama Petugas","Kode Laporan","Alamat Kejadian","Tanggal Kejadian","Kode Mobil","Plat"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelpenanganan.setModel(tabmode);
        String sql = "select * from penanganan";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("kd_p");
                String b = hasil.getString("id_ptgs");
                String c = hasil.getString("nama");
                String d = hasil.getString("kode_laporan");
                String e = hasil.getString("alamat");
                String f = hasil.getString("tgl");
                String g = hasil.getString("nomor");
                String h = hasil.getString("plat");
                                               
                String[] data = {a,b,c,d,e,f,g,h};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

