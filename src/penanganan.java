    private void tabelpenangananKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabelpenangananKeyPressed
        if (evt.getKeyCode()==KeyEvent.VK_DOWN || evt.getKeyCode()==KeyEvent.VK_UP);
        try{
            int row =tabelpenanganan.getSelectedRow();
            String Table_click=(tabelpenanganan.getModel().getValueAt(row, 0).toString());
            String sql="select * from penanganan where kd_p='"+Table_click+"'";
            pst=conn.prepareStatement(sql);
        rs=pst.executeQuery();
        if(rs.next()){
            String add1 =rs.getString("kd_p");
            txt_kdp.setText(add1);
            String add2 =rs.getString("id_ptgs");
            txt_kdpetugas.setText(add2);
            String add3 =rs.getString("nama");
            txt_nmp.setText(add3);
            String add4 =rs.getString("kode_laporan");
            txt_kode_laporan.setText(add4);
            String add5 =rs.getString("alamat");
            txt_alamat.setText(add5);
            String add6 =rs.getString("tgl");
            txt_date.setText(add6);
            String add7 =rs.getString("nomor");
            txt_nomor.setText(add7);
            String add8 =rs.getString("plat");
            txt_plat.setText(add8);
                    }
        } catch (Exception e){
          JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tabelpenangananKeyPressed

