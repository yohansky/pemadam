    private void cmd_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_deleteActionPerformed
        int row =tabel_evakuasi.getSelectedRow();
        int p = JOptionPane.showConfirmDialog(null,"Apakah Anda Yaqin?","Delete",JOptionPane.YES_NO_OPTION);
        if(p==0){
            String Table_click=(tabel_evakuasi.getModel().getValueAt(row, 0).toString());
            String sql="delete from evakuasi where kd_e='"+Table_click+"'";
            try {
                pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Deleted");

            }catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            Update_table();
            datatable();
        }
    }//GEN-LAST:event_cmd_deleteActionPerformed

