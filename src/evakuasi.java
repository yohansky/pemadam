    private void tabel_evakuasiKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabel_evakuasiKeyPressed
        if (evt.getKeyCode()==KeyEvent.VK_DOWN || evt.getKeyCode()==KeyEvent.VK_UP);
        try{
            int row =tabel_evakuasi.getSelectedRow();
            String Table_click=(tabel_evakuasi.getModel().getValueAt(row, 0).toString());
            String sql="select * from evakuasi where kd_e='"+Table_click+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                String add1 =rs.getString("kd_e");
                txt_kde.setText(add1);
                String add2 =rs.getString("kd_l");
                txt_kode_laporan.setText(add2);
                String add3 =rs.getString("tgl");
                txt_date.setText(add3);
                String add4 =rs.getString("alamat");
                txt_alamat.setText(add4);
                String add5 =rs.getString("nik");
                txt_nik.setText(add5);
                String add6 =rs.getString("nm_k");
                txt_nm.setText(add6);
                String add7 =rs.getString("u_k");
                txt_usia.setText(add7);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tabel_evakuasiKeyPressed

