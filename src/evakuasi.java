    private void datatable(){
        Object[] Baris = {"Kode Evakuasi","Kode Laporan","Tanggal Kejadian","Alamat Kejadian","Nomor Induk Kependudukan","Nama Korban","Usia Korban"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_evakuasi.setModel(tabmode);
        String sql = "select * from evakuasi";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("kd_e");
                String b = hasil.getString("kd_l");
                String c = hasil.getString("tgl");
                String d = hasil.getString("alamat");
                String e = hasil.getString("nik");
                String f = hasil.getString("nm_k");
                String g = hasil.getString("u_k");
                                               
                String[] data = {a,b,c,d,e,f,g};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

